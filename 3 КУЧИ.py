def f(a,b,c,m):
    if a+b+c>=71: return m%2==0  #менять 71 — граница суммы трёх куч
    if m==0: return 0             #не трогать
    h = [f(a+3,b,c,m-1),f(a*2,b,c,m-1),  #ходы с кучей a: +3 и *2
         f(a,b+3,c,m-1),f(a,b*2,c,m-1),  #ходы с кучей b: +3 и *2
         f(a,b,c+3,m-1),f(a,b,c*2,m-1)]  #ходы с кучей c: +3 и *2
    #менять +3 и *2 на ходы из задачи (одинаково для всех трёх куч)
    return any(h) if (m-1)%2==0 else all(h)  #не трогать
    #если в 19 вышло [] — поменяй all(h) на any(h)

print('19',[s for s in range(1,59) if f(7,5,s,2)])               #менять: 7 — первая куча, 5 — вторая куча, 59 — максимальный S+1
print('20',[s for s in range(1,59) if not f(7,5,s,1) and f(7,5,s,3)]) #менять: 7, 5 и 59 так же
print('21',[s for s in range(1,59) if not f(7,5,s,2) and f(7,5,s,4)]) #менять: 7, 5 и 59 так же
