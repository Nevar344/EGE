# Отличие — нельзя повторять ход который сам же делал в прошлый раз (не противник, а ты сам)
def f(s,m,p1,p2):
    if s>=121: return m%2==0  #менять 121 — граница
    if m==0: return 0          #не трогать
    h = []
    if p2!='+2':  h+=[f(s+2, m-1,'+2', p1)]   #менять ходы
    if p2!='+5':  h+=[f(s+5, m-1,'+5', p1)]   #менять ходы
    if p2!='+12': h+=[f(s+12,m-1,'+12',p1)]   #менять ходы
    if p2!='*2':  h+=[f(s*2, m-1,'*2', p1)]   #менять ходы
    return any(h) if m%2!=0 else all(h)  #не трогать

print('19',[s for s in range(1,121) if f(s,2,'','')])                       #менять только 121
