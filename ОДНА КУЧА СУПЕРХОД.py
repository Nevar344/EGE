# Отличие — есть суперход (пропуск хода) который можно использовать только один раз за игру
def f(s,m,p):
    if s>=20: return m%2==0  #менять 20 — граница
    if m==0: return 0         #не трогать
    h = [f(s+2,m-1,p), f(s*2,m-1,p)]  #менять ходы из задачи, p не трогать
    if p!=1: h += [f(s,m-1,1)]        #суперход — пропуск хода, можно только один раз
    #p!=1 означает суперход ещё не использован
    #не трогать эту строку
    return any(h) if m%2!=0 else all(h)  #не трогать
    #для вопроса 19 (any/any) — меняй all(h) на any(h), потом возвращай обратно

print('19',[s for s in range(1,20) if f(s,2,0)])                       #менять только 20
print('20',[s for s in range(1,20) if not f(s,1,0) and f(s,3,0)])     #менять только 20
print('21',[s for s in range(1,20) if f(s,20,0)])                      #менять только 20
