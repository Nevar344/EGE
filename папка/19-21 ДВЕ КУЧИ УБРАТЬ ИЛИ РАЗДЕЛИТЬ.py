# Отличие — ходы убирают камни или делят кучу пополам
def f(a,b,m):
    if a==0 or b==0: return m%2==0  #не трогать — игра кончается когда одна куча пустая
    h = []
    if a>=3 and b>=3: h+=[f(a-3,b-3,m-1)]  #менять 3 — убрать из КАЖДОЙ кучи по 3
    if a%2==0: h+=[f(a//2,a//2,m-1)]        #разделить кучу a пополам — только если чётная
    if b%2==0: h+=[f(b//2,b//2,m-1)]        #разделить кучу b пополам — только если чётная
    #ВАЖНО: a//2,a//2 — куча a делится на две равные, b исчезает
    #       b//2,b//2 — куча b делится на две равные, a исчезает
    if len(h)==0: return m%2==0  #не трогать — если ходов нет игра тоже кончается
    if m==0: return 0             #не трогать
    return any(h) if m%2!=0 else all(h)  #не трогать
    #для вопроса 19 — меняй all(h) на any(h), потом возвращай обратно

print('19',[k for k in range(1,100) if f(32,k,2)])                        #менять: 32 — N из задачи, 100 — достаточно большое
print('20',[k for k in range(1,100) if not f(32,k,1) and f(32,k,3)])      #менять: 32 и 100
print('21',[k for k in range(1,100) if not f(20,k,2) and f(20,k,4)])      #менять: 20 — N из вопроса 3, 100

