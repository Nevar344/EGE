def f(a,b,m):
    if a==b: return m%2==0   #не трогать — игра кончается когда кучи равны
    if m==0: return 0         #не трогать
    if a<b: h = [f(a+1,b,m-1),f(a+3,b,m-1)]  #менять +1 и +3 на ходы из задачи
    else:   h = [f(a,b+1,m-1),f(a,b+3,m-1)]  #менять +1 и +3 на те же ходы
    return any(h) if (m-1)%2==0 else all(h)  #не трогать
    #если в 19 вышло [] — поменяй all(h) на any(h) в этой строке

l19 = [s for s in range(1,24) if not f(13,s,0) and f(13,s,2)]  #менять: 13 — первая куча, 24 — максимальный S+1
l20 = [s for s in range(1,24) if not f(13,s,1) and f(13,s,3)]  #менять: 13 и 24 так же
l21 = [s for s in range(1,24) if not f(13,s,2) and f(13,s,4)]  #менять: 13 и 24 так же
print('19', l19)
