# Отличие — нельзя делать тот же ход что сделал противник
def f(s,m,p):
    if s>=140: return m%2==0  #менять 140 — граница
    if m==0: return 0          #не трогать
    h = []                     #не трогать — список ходов начинается пустым
    if p!='+1': h+=[f(s+1,m-1,'+1')]  #менять +1 и '+1' на свой ход
    if p!='+2': h+=[f(s+2,m-1,'+2')]  #менять +2 и '+2' на свой ход
    if p!='*3': h+=[f(s*3,m-1,'*3')]  #менять *3 и '*3' на свой ход
    #каждая строка — один ход, если ходов больше/меньше — добавляй/убирай строки
    #названия в кавычках '+1','+2','*3' — просто метки, главное чтоб совпадали
    return any(h) if m%2!=0 else all(h)  #не трогать

print('19',[s for s in range(1,140) if f(s,2,'')])               #менять только 140
print('20',[s for s in range(1,140) if not f(s,1,'') and f(s,3,'')])  #менять только 140
print('21',[s for s in range(1,140) if not f(s,2,'') and f(s,4,'')])  #менять только 140
