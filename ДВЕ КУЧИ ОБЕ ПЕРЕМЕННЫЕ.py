#Как отличать по тексту "K≥1, S≥1" — обе переменные, вопрос 1 про количество пар
def f(a,b,m):
    if a+b>=45: return m%2==0  #менять 45 — граница суммы из задачи
    if m==0: return 0           #не трогать
    h = [f(a+2,b,m-1),f(a*3,b,m-1),f(a,b+2,m-1),f(a,b*3,m-1)]
    #менять +2 и *3 на ходы из задачи (одинаково для обеих куч)
    return any(h) if (m-1)%2==0 else all(h)  #не трогать

print('19',len([(k,s) for k in range(1,44) for s in range(1,44) if not f(k,s,0) and f(k,s,2)]))
#не трогать структуру, менять только 44 (это граница 45 минус 1 = 44)
#len() — потому что вопрос 1 спрашивает КОЛИЧЕСТВО пар

print('20',[s for s in range(1,44) if not f(4,s,1) and f(4,s,3)])
#менять: 4 — это K который дан в вопросе 2, 44 — граница-1

